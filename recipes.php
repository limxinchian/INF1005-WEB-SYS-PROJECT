    <?php
        session_start();    
        require_once 'config/db.php'; // Fixed the path to avoid directory errors

        if (!isset($_SESSION['user_id'])) {
            header('Location: login.php');
            exit;
        }

        $selected_tag = isset($_GET['tag']) ? htmlspecialchars(trim($_GET['tag'])) : '';

        $recipes = [];
        $error_msg = "";

        // V2 update: Query the View instead of the raw table so we get the pre-formatted dietary_tags
        $sql = "SELECT DISTINCT r.* FROM recipes r
                LEFT JOIN recipe_ingredients ri ON r.recipe_id = ri.recipe_id
                LEFT JOIN ingredient_allergens ia ON ia.ingredient_id = ri.ingredient_id
                LEFT JOIN allergens a ON a.allergen_id = ia.allergen_id WHERE ";

        // V2 update: Added deleted_at IS NULL to hide soft-deleted recipes
        $where_clauses = ["r.deleted_at IS NULL", "r.submitted_by = ?"];
        $params = [$_SESSION['user_id']];

        // If they click a sidebar tag (like Vegan), apply that filter
        if (!empty($selected_tag)) {
            // These 3 tags map directly to negative Allergen filters
            if (in_array($selected_tag, ['Nut-Free', 'Gluten-Free', 'Dairy-Free'])) {
                
                $allergen_map = [
                    'Nut-Free' => 'Nuts',
                    'Gluten-Free' => 'Gluten',
                    'Dairy-Free' => 'Dairy'
                ];
                $target_allergen = $allergen_map[$selected_tag];

                // V2 update: Exclude recipes that contain ingredients linked to this specific allergen
                $where_clauses[] = "r.recipe_id NOT IN (
                    SELECT ri.recipe_id 
                    FROM recipe_ingredients ri
                    INNER JOIN ingredient_allergens ia ON ia.ingredient_id = ri.ingredient_id
                    INNER JOIN allergens a ON a.allergen_id = ia.allergen_id
                    WHERE a.allergen_name = ?
                )";
                $params[] = $target_allergen;

            } else {
                // INCLUSION: For Vegan, Vegetarian, Halal, Keto, and Paleo
                // V2 update: Just search the concatenated tags column generated by the View
                $where_clauses[] = "r.dietary_tags LIKE ?";
                $params[] = "%" . $selected_tag . "%";
            }
        }

        $sql .= implode(" AND ", $where_clauses);

        // PDO: prepare once, then execute with the full $params array
        try {
            $stmt = $pdo->prepare($sql);
            $stmt->execute($params);
            while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
                $recipes[] = $row;
            }
            $stmt = null;
        } catch (PDOException $e) {
            $error_msg = "Database error: " . $e->getMessage();
        }

        $approved_recipes = array_filter($recipes, fn($r) => $r['status'] === 'approved');
        $rejected_recipes = array_filter($recipes, fn($r) => $r['status'] === 'rejected');

        $title = "MealMate - My Recipes";
        include_once 'includes/header.php';
        require_once 'helper/get-image-link.php';
    ?>
    <link rel="stylesheet" href="assets/css/recipes.css">
</head>
