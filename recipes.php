            <div class="d-flex flex-row justify-content-between align-items-center">
                <h2 class="mb-4">
                    All Approved Recipes
                </h2>
                <a href="submit-recipe.php" class="btn btn-primary text-nowrap flex-shrink-0">Add Recipe</a>
            </div>

            <?php if (!empty($error_msg)): ?>
                <div class="alert alert-danger"><?php echo $error_msg; ?></div>
            <?php endif; ?>

            <div class="row">
                <?php if (empty($approved_recipes) && empty($error_msg)): ?>
                    <div class="col-12">
                        <div class="alert alert-info">
                            No approved recipes found. Either it is pending approvel, rejected, or you can submit your first at <a href="submit-recipe.php" class="alert-link">Submit Recipe</a>!
                        </div>
                    </div>
                <?php else: ?>
                    
                    <?php foreach ($approved_recipes as $recipe): ?>
                        <div class="col-md-6 col-lg-4 mb-4">
                            <div class="card h-100 shadow-sm">
                                <div class="rounded-top overflow-hidden recipe-images">
                                    <img src="<?= htmlspecialchars(getImageLink($recipe['title'], $recipe['recipe_id'])) ?>" alt="<?= htmlspecialchars($recipe['title']) ?>" style="width:100%; height:100%; object-fit:cover;">
                                </div>
                                <div class="card-body d-flex flex-column">
                                    <h5 class="card-title"><?php echo htmlspecialchars($recipe['title']); ?></h5>
                                    
                                    <p class="card-text text-primary small mb-1 fw-bold">
                                        <?php echo htmlspecialchars($recipe['dietary_tags'] ?? ''); ?>
                                    </p>

                                    <p class="card-text text-muted small mb-2">
                                        Prep Time: <?php echo htmlspecialchars($recipe['prep_time_min']); ?> mins
                                    </p>
                                    <p class="card-text flex-grow-1">
                                        <?php echo htmlspecialchars(substr($recipe['description'] ?? 'No description available.', 0, 75)) . '...'; ?>
                                    </p>
                                    <a href="recipe-detail.php?id=<?php echo $recipe['recipe_id']; ?>" class="btn btn-outline-primary mt-auto">View Full Recipe</a>
                                </div>
                            </div>
                        </div>
                    <?php endforeach; ?>
                    
                <?php endif; ?>
            </div>
        </div>
    </div>

    <div class="container-fluid px-4 mt-3">
        <div class="">
            <h2 class="mb-4">
                All Rejected Recipes
            </h2>

            <?php if (!empty($error_msg)): ?>
                <div class="alert alert-danger"><?php echo $error_msg; ?></div>
            <?php endif; ?>

            <div class="row">
                <?php if (empty($rejected_recipes) && empty($error_msg)): ?>
                    <div class="col-12">
                        <div class="alert alert-info">
                            No rejected recipes.
                        </div>
                    </div>
                <?php else: ?>
                    
                    <?php foreach ($rejected_recipes as $recipe): ?>
                        <div class="col-md-6 col-lg-4 mb-4">
                            <div class="card h-100 shadow-sm">
                                <div class="rounded-top overflow-hidden recipe-images">
                                    <img src="<?= htmlspecialchars(getImageLink($recipe['title'], $recipe['recipe_id'])) ?>" alt="<?= htmlspecialchars($recipe['title']) ?>" style="width:100%; height:100%; object-fit:cover;">
                                </div>
                                <div class="card-body d-flex flex-column">
                                    <h5 class="card-title"><?php echo htmlspecialchars($recipe['title']); ?></h5>
                                    
                                    <p class="card-text text-primary small mb-1 fw-bold">
                                        <?php echo htmlspecialchars($recipe['dietary_tags'] ?? ''); ?>
                                    </p>

                                    <p class="card-text text-muted small mb-2">
                                        Prep Time: <?php echo htmlspecialchars($recipe['prep_time_min']); ?> mins
                                    </p>
                                    <p class="card-text flex-grow-1">
                                        <?php echo htmlspecialchars(substr($recipe['description'] ?? 'No description available.', 0, 75)) . '...'; ?>
                                    </p>
                                    <a href="recipe-detail.php?id=<?php echo $recipe['recipe_id']; ?>" class="btn btn-outline-primary mt-auto">View Full Recipe</a>
                                </div>
                            </div>
                        </div>
                    <?php endforeach; ?>
                    
                <?php endif; ?>
            </div>
        </div>
